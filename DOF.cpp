#include "DOF.h"


////////////////////////////////////////////////////////////////////////////////
//constructors
////////////////////////////////////////////////////////////////////////////////
DOF::DOF() {
    max = MAX_DOF;
    min = MIN_DOF;
    pose = 0;
}

////////////////////////////////////////////////////////////////////////////////
//set variables
////////////////////////////////////////////////////////////////////////////////
float DOF::getRotLimitMin() {
    return min;
}

float DOF::getRotLimitMax() {
    return max;
}

float DOF::getPose() {
    float temp = pose;
    if (temp < min) temp = min;
    if (temp > max) temp = max;
    return temp;
}

////////////////////////////////////////////////////////////////////////////////
//set variables
////////////////////////////////////////////////////////////////////////////////
void DOF::setPose(float pos) {
    pose = pos;

}

void DOF::setRotLimitMin(float m) {
    min = m;
}

void DOF::setRotLimitMax(float m) {
    max = m;
}


